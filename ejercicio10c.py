def obtener_nombre_mes():
    numero_mes = int(input("Por favor, ingresa el número del mes: "))

    if numero_mes == 1:
        print("Enero")
    elif numero_mes == 2:
        print("Febrero")
    elif numero_mes == 3:
        print("Marzo")
    elif numero_mes == 4:
        print("Abril")
    elif numero_mes == 5:
        print("Mayo")
    elif numero_mes == 6:
        print("Junio")
    elif numero_mes == 7:
        print("Julio")
    elif numero_mes == 8:
        print("Agosto")
    elif numero_mes == 9:
        print("Septiembre")
    elif numero_mes == 10:
        print("Octubre")
    elif numero_mes == 11:
        print("Noviembre")
    elif numero_mes == 12:
        print("Diciembre")
    else:
        print("Mes inválido")

obtener_nombre_mes()
